public class Funcionario { 

     String nome; 

     int numinscrição; 

     double salario; 

     int turno; 

      public Funcionario(String nome, int numinscrição, double salario, int turno) { 

        this.nome = nome; 

        this.numinscrição = numinscrição; 

        this.salario = salario; 

        this.turno = turno; 

    } 

    public String getNome() { 

        return nome; 

    } 

    public int getNuminscrição() { 

        return numinscrição; 

    } 

    public double getSalario() { 

        return salario; 

    } 

    public int getTurno() { 

        return turno; 

    } 

    public void setNome(String nome) { 

        this.nome = nome; 

    } 

    public void setNuminscrição(int numinscrição) { 

        this.numinscrição = numinscrição; 

    } 

    public void setSalario(double salario) { 

        this.salario = salario; 

    } 

    public void setTurno(int turno) { 

        this.turno = turno; 

    } 

   

 
 

     

} 

 
